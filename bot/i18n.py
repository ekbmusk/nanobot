"""Интернационализация — қазақша / орысша мәтіндер."""


def t(key: str, lang: str = "kk") -> str:
    """Аудармасын алу."""
    return TEXTS.get(lang, TEXTS["kk"]).get(key, TEXTS["kk"].get(key, key))


def get_text(obj: dict, field: str, lang: str = "kk") -> str:
    """JSON объектісінен тілге сай мәтінді алу.

    Мысалы: get_text(question, "text", "ru") → question["text_ru"] немесе question["text"]
    """
    if lang == "ru":
        ru_key = f"{field}_ru"
        if ru_key in obj:
            return obj[ru_key]
    return obj.get(field, "")


